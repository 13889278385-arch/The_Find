#!/usr/bin/env bash
# ============================================================
# Linux / macOS 一键启动脚本
# ============================================================
# set -e 表示：只要有任意一条命令失败，脚本就立即退出。
# 这样能避免“前面已经失败，后面还继续执行”带来的混乱状态。
set -e

# 进入脚本所在目录。
# $(dirname "$0") 取的是当前脚本文件所在文件夹。
cd "$(dirname "$0")"

# 安装 Python 依赖。
python3 -m pip install -r requirements.txt

# 启动 Flask 应用。
python3 app.py
